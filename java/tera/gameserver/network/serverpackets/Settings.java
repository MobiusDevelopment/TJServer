package tera.gameserver.network.serverpackets;

import java.nio.ByteBuffer;

import tera.gameserver.model.playable.Player;
import tera.gameserver.network.ServerPacketType;

/**
 * @author Ronn
 */
public class Settings extends ServerPacket
{
	private static final ServerPacket instance = new Settings();
	
	/**
	 * Method getInstance.
	 * @param player Player
	 * @return Settings
	 */
	public static Settings getInstance(Player player)
	{
		final Settings packet = (Settings) instance.newInstance();
		packet.setting = player.getSettings();
		return packet;
	}
	
	private byte[] setting;
	
	/**
	 * Method finalyze.
	 * @see rlib.util.pools.Foldable#finalyze()
	 */
	@Override
	public void finalyze()
	{
		setting = null;
	}
	
	/**
	 * Method getPacketType.
	 * @return ServerPacketType
	 */
	@Override
	public ServerPacketType getPacketType()
	{
		return ServerPacketType.SETTINGS;
	}
	
	/**
	 * Method isSynchronized.
	 * @return boolean
	 * @see rlib.network.packets.SendablePacket#isSynchronized()
	 */
	@Override
	public boolean isSynchronized()
	{
		return false;
	}
	
	/**
	 * Method writeImpl.
	 * @param buffer ByteBuffer
	 */
	@Override
	protected void writeImpl(ByteBuffer buffer)
	{
		if (setting != null)
		{
			writeOpcode(buffer);
			buffer.put(setting);
		}
	}
}
